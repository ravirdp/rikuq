// Generate the rikuq favicon set from a single inline SVG source.
//
// Replaces the Citare-branded bundle that accidentally ended up in public/
// from a realfavicongenerator.net run using the wrong source image.
//
// Design: lowercase amber 'r' on near-black square. Reads as a brand mark
// at 16x16, recognizable as rikuq at any size. Matches the wordmark's
// 'amber accent' visual language.
//
// Output:
//   public/favicon.svg                        — modern browsers (scalable)
//   public/favicon-96x96.png                  — high-DPI tab fallback
//   public/apple-touch-icon.png               — 180x180 iOS home screen
//   public/web-app-manifest-192x192.png       — Android home screen
//   public/web-app-manifest-512x512.png       — Android splash / PWA
//
// Run: node scripts/generate-favicons.mjs

import sharp from 'sharp';
import { writeFile } from 'node:fs/promises';

const BG = '#0a0b0e';          // site near-black
const FG = '#f5b942';          // brand amber
const ACCENT = '#ececec';      // optional cream dot above 'r'

// Scalable SVG source — drives both the favicon.svg deliverable and every
// PNG variant. Designed for a 512x512 viewBox; sharp resamples down for
// each PNG size.
const svgSource = `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 512 512" role="img" aria-label="rikuq">
  <rect width="512" height="512" rx="96" fill="${BG}"/>
  <text x="256" y="386"
        text-anchor="middle"
        font-family="Inter, Helvetica, Arial, sans-serif"
        font-weight="800"
        font-size="400"
        letter-spacing="-20"
        fill="${FG}">r</text>
  <!-- Cream accent dot above the 'r' — matches the wordmark's amber/cream
       interplay. Sized to read even at favicon scale. -->
  <circle cx="278" cy="118" r="36" fill="${ACCENT}"/>
</svg>`;

// 1. Write the SVG verbatim (modern browsers prefer this — scales perfectly).
await writeFile('public/favicon.svg', svgSource, 'utf8');
console.log('✅ public/favicon.svg');

// 2. Rasterize every PNG variant from the SAME source SVG.
const variants = [
  { path: 'public/favicon-96x96.png',                size: 96 },
  { path: 'public/apple-touch-icon.png',             size: 180 },
  { path: 'public/web-app-manifest-192x192.png',     size: 192 },
  { path: 'public/web-app-manifest-512x512.png',     size: 512 },
];

for (const { path, size } of variants) {
  await sharp(Buffer.from(svgSource))
    .resize(size, size)
    .png({ compressionLevel: 9 })
    .toFile(path);
  console.log(`✅ ${path} (${size}x${size})`);
}

console.log('\nNote: public/favicon.ico is NOT regenerated by this script.');
console.log('Modern browsers prefer favicon.svg. If you want a fresh .ico,');
console.log('the easiest path is realfavicongenerator.net using');
console.log('public/web-app-manifest-512x512.png as the source.');
